package com.gewara.web.action.partner;

public class PartnerQihu360CPSModel {
	// 合作网站编号
	private String bid;
	// 360业务编号
	private String qihoo_id;
	// 用户最终访问url
	private String url;
	// 请求来源url
	private String from_url;
	// 调用发起时间，精确到秒
	private String active_time;
	// 扩展字符串
	private String ext;
	// 360用户ID
	private String qid;
	// 用户邮箱
	private String qmail;
	// 用户显示名称
	private String qname;
	// 签名信息
	private String sign;
	public String getBid() {
		return bid;
	}
	public void setBid(String bid) {
		this.bid = bid;
	}
	public String getQihoo_id() {
		return qihoo_id;
	}
	public void setQihoo_id(String qihoo_id) {
		this.qihoo_id = qihoo_id;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getFrom_url() {
		return from_url;
	}
	public void setFrom_url(String from_url) {
		this.from_url = from_url;
	}
	public String getActive_time() {
		return active_time;
	}
	public void setActive_time(String active_time) {
		this.active_time = active_time;
	}
	public String getExt() {
		return ext;
	}
	public void setExt(String ext) {
		this.ext = ext;
	}
	public String getQid() {
		return qid;
	}
	public void setQid(String qid) {
		this.qid = qid;
	}
	public String getQmail() {
		return qmail;
	}
	public void setQmail(String qmail) {
		this.qmail = qmail;
	}
	public String getQname() {
		return qname;
	}
	public void setQname(String qname) {
		this.qname = qname;
	}
	public String getSign() {
		return sign;
	}
	public void setSign(String sign) {
		this.sign = sign;
	}
	
}
